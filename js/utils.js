function drawRandNum(nums) {
    var randIdx = getRandomInt(0, nums.length)
    return nums.splice(randIdx, 1)[0]

}

function getRandomInt(min, max) {
    min = Math.ceil(min);
    max = Math.floor(max);
    return Math.floor(Math.random() * (max - min)) + min;
}
